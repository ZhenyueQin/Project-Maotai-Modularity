class ClimbingAnalyzer:
    def __init__(self):
        pass

    def generate_blocks(self, fitness_value_lists):
        blocks = []

        step = 0
        for a_fitness_value_list in fitness_value_lists:
            for fitness_value in a_fitness_value_list:
                blocks.append((step, fitness_value))
            step += 1

        return blocks

